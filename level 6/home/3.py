distance_centimeters = distance_km * 100000
distance_millimeters = distance_km * 1000000
print(f"მანძილი სახლიდან სკოლამდე მეტრებში: {distance_meters}")
print(f"მანძილი სახლიდან სკოლამდე სანტიმეტრებში: {distance_centimeters}")
print(f"მანძილი სახლიდან სკოლამდე მილიმეტრებში: {distance_millimeters}")
